struct State {
  unsigned long x;      // x coordinate
	unsigned long hitbox[2]; // range of x and y values
  unsigned long y;      // y coordinate
  const unsigned char *image[2]; // ptr->image
  long life;            // 0=dead, 1=alive
};          
typedef struct State STyp;
STyp Enemy[4];
STyp Bunker;
